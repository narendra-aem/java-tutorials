class SwapwithNV {

   public static void main(String args[])
   {
      int a=10;
	  int b=20;
	  int c;
	  
      System.out.println("Value of a: " + a);
	  System.out.println("Value of b: " + b);
	  c=a;
	  a=b;
	  b=c;
	  System.out.println("Value of a: " + a);
	  System.out.println("Value of b: " + b);
	  
   }
}